
# 以深色主题背景观看 YouTube 视频

如果启用深色主题背景，您可以减少屏幕的眩光，体验深色背景下的 YouTube。

计算机 

1. 选择您的个人资料照片 ![|18x18](https://lh3.googleusercontent.com/NB5qyD2bwPLSxRz3L4RkFWHtTntWnKPJ5-jUmi5tToCc3-230ToGVw1WbpGWolgh2eT4=w18-h18)。
2. 选择 **深色主题** 。
3. 找到“启用深色主题背景”，将其开启或关闭。


Android 设备

**对于大多数 Android 用户：**

1. 选择您的个人资料照片 ![|18x18](https://lh3.googleusercontent.com/NB5qyD2bwPLSxRz3L4RkFWHtTntWnKPJ5-jUmi5tToCc3-230ToGVw1WbpGWolgh2eT4=w18-h18)。
2. 点按 **设置** 图标 ![|18x18](https://lh3.googleusercontent.com/JIfhFcNpFpZRX6J6zdHg7aTr4kToTU05MJCZYULcdbQ8HFScPP4QEyJK0vwQaSAS9w=w18-h18)。
3. 点按 **常规** 。
4. 点按 **深色主题背景** 以开启或关闭此功能。

**对于 Android 10 用户：**

1. 选择您的个人资料照片 ![|18x18](https://lh3.googleusercontent.com/NB5qyD2bwPLSxRz3L4RkFWHtTntWnKPJ5-jUmi5tToCc3-230ToGVw1WbpGWolgh2eT4=w18-h18)。
2. 点按 **设置** 图标 ![|18x18](https://lh3.googleusercontent.com/JIfhFcNpFpZRX6J6zdHg7aTr4kToTU05MJCZYULcdbQ8HFScPP4QEyJK0vwQaSAS9w=w18-h18)。
3. 点按 **常规** 。
4. 点按 **外观** 。
5. 选择“使用设备主题背景”以使用设备级别的深色主题背景设置，
或者  在 YouTube 内开启 **浅色** 或 **深色** 主题背景。

iPhone 和 iPad

1. 选择您的个人资料照片 ![|18x18](https://lh3.googleusercontent.com/NB5qyD2bwPLSxRz3L4RkFWHtTntWnKPJ5-jUmi5tToCc3-230ToGVw1WbpGWolgh2eT4=w18-h18)。
2. 点按 **设置** 图标 ![|18x18](https://lh3.googleusercontent.com/JIfhFcNpFpZRX6J6zdHg7aTr4kToTU05MJCZYULcdbQ8HFScPP4QEyJK0vwQaSAS9w=w18-h18)。
3. 点按 **深色主题背景** 以开启或关闭此功能。